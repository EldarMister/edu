export type TFunc = (ru: string) => string;

/** Хук перевода: t('Русская строка') → перевод по текущей локали. */
export function useT(): TFunc {
  const locale = useLocale((s) => s.locale);
  return (ru: string) => (locale === 'ky' ? KY[ru] ?? ru : ru);
}
